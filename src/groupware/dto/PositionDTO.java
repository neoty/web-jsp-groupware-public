package groupware.dto;

public class PositionDTO {
	
	private int posi_level;
	private String posi_name;			
	private int posi_use;
	
	public int getPosi_level() {
		return posi_level;
	}
	public void setPosi_level(int posi_level) {
		this.posi_level = posi_level;
	}
	public String getPosi_name() {
		return posi_name;
	}
	public void setPosi_name(String posi_name) {
		this.posi_name = posi_name;
	}
	public int getPosi_use() {
		return posi_use;
	}
	public void setPosi_use(int posi_use) {
		this.posi_use = posi_use;
	}
	
}
